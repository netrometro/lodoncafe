          <h1>História do Café</h1>
        <p>O café é originário das terras altas da Etiópia, em um local chamado Kaffa. Porém, a palavra "café" não é originária de Kaffa, e sim da palavra árabe qahwa, que significa "vinho". Por esse motivo, o café era conhecido como "vinho da Arábia" quando chegou à Europa no Século XIV.</p>

<p>Uma lenda conta que um pastor chamado Kaldi observou que seus carneiros ficavam saltitantes e conseguiam percorrer longas distâncias ao comer as folhas e frutos do cafeeiro. Ele experimentou os frutos e sentiu maior vivacidade. Um monge da região, informado sobre o fato, começou a utilizar uma infusão de frutos para resistir ao sono enquanto orava.</p>

<p>Parece que as tribos africanas, que conheciam o café desde a Antiguidade, moíam seus grãos e faziam uma pasta utilizada para alimentar os animais e aumentar as forças dos guerreiros.</p>

<p>Seu cultivo se estendeu primeiro na Arábia, onde os manuscritos mais antigos mencionando a cultura do café datam de 575 no Iêmen. Até então, era comum o consumo da fruta in natura.</p>

<p>O conhecimento dos efeitos do café disseminaram-se e no século XVI o café foi levado a península arábica, sendo torrado para se transformar em bebida pela primeira vez na Pérsia.</p>

<p>Na Arábia, a infusão do café recebeu o nome de kahwah ou cahue (ou ainda qah'wa, do original em árabe). Enquanto na língua turco otomana era conhecido como kahve, cujo significado original também era "vinho". A classificação Coffea arabica foi dada pelo naturalista Lineu.</p>

<p>O café, no entanto, teve inimigos mesmo entre os árabes, que consideravam suas propriedades contrárias às leis do profeta Maomé. No entanto, logo o café venceu essas resistências e até os doutores muçulmanos aderiram à bebida para favorecer a digestão, alegrar o espírito e afastar o sono, segundo os escritores da época.</p>
